n = float(input('Digite a distancia em metros: '))

print('Essa distancia em {:.3f}Km'.format(n/1000))
print('Essa distancia em {:.3f}Hm'.format(n/100))
print('Essa distancia em {:.3f}Dam'.format(n/10))
print('Essa distancia em {:.3f}dm'.format(n*10))
print('Essa distancia em {:.2f}cm'.format(n*100))
print('Essa distancia em {:.2f}mm'.format(n*1000))
