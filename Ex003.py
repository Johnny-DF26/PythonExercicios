
n1 = int(input('Digite um valor: '))
n2 = int(input('Digite outro valor: '))

soma = n1 + n2
sub = n1 - n2
mult = n1 * n2
div = n1 / n2

print('O valor {} + {} é: {}'.format(n1,n2,soma))
print('O valor {} - {} é: {}'.format(n1,n2,sub))
print('O valor {} multiplicado por {} é: {}'.format(n1,n2,mult))
print('O valor {} dividido por {} é: {}'.format(n1,n2,div))



